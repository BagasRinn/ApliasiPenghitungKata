    private void hitungOtomatis() {
        String teks = txtTeks.getText().trim();
        if (teks.isEmpty()) {
            lblKata.setText("0 kata");
            lblKarakter.setText("0 karakter");
            lblKalimat.setText("0 kalimat");
            lblParagraf.setText("0 paragraf");
            return;
        }

        lblKata.setText(teks.split("\\s+").length + " kata");
        lblKarakter.setText(teks.length() + " karakter");
        lblKalimat.setText(teks.split("[.!?]+").length + " kalimat");
        lblParagraf.setText(teks.split("\\n+").length + " paragraf");
    }
