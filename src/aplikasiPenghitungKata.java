    private void btnHitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHitungActionPerformed
        String teks = txtTeks.getText().trim();

        if (teks.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Teks masih kosong!");
            return;
        }

        // Hitung jumlah kata (pisahkan berdasarkan spasi)
        String[] kata = teks.split("\\s+");
        int jumlahKata = kata.length;

        // Hitung karakter (termasuk spasi)
        int jumlahKarakter = teks.length();

        // Hitung kalimat (berdasarkan tanda titik, seru, atau tanya)
        String[] kalimat = teks.split("[.!?]+");
        int jumlahKalimat = kalimat.length;

        // Hitung paragraf (berdasarkan baris kosong)
        String[] paragraf = teks.split("\\n+");
        int jumlahParagraf = paragraf.length;

        // Tampilkan hasil
        lblKata.setText(jumlahKata + " kata");
        lblKarakter.setText(jumlahKarakter + " karakter");
        lblKalimat.setText(jumlahKalimat + " kalimat");
        lblParagraf.setText(jumlahParagraf + " paragraf");
    }//GEN-LAST:event_btnHitungActionPerformed

    private void btnCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariActionPerformed
        String teks = txtTeks.getText().toLowerCase();
        String cari = txtCari.getText().toLowerCase();

        if (cari.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Masukkan kata yang ingin dicari!");
            return;
        }

        int count = 0;
        int index = teks.indexOf(cari);
        while (index != -1) {
            count++;
            index = teks.indexOf(cari, index + 1);
        }

        lblHasilCari.setText("Kata \"" + cari + "\" ditemukan sebanyak " + count + " kali");
    }//GEN-LAST:event_btnCariActionPerformed

    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        JFileChooser chooser = new JFileChooser();
        int pilihan = chooser.showSaveDialog(this);
        if (pilihan == JFileChooser.APPROVE_OPTION) {
            try (FileWriter fw = new FileWriter(chooser.getSelectedFile())) {
                fw.write(txtTeks.getText());
                fw.write("\n\n=== HASIL PERHITUNGAN ===\n");
                fw.write("Kata: " + lblKata.getText() + "\n");
                fw.write("Karakter: " + lblKarakter.getText() + "\n");
                fw.write("Kalimat: " + lblKalimat.getText() + "\n");
                fw.write("Paragraf: " + lblParagraf.getText() + "\n");
                JOptionPane.showMessageDialog(this, "Berhasil disimpan!");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Gagal menyimpan file!");
            }
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

